
##Access Items
List =['a','b','c','d','e','f']
print(List[1])

#last item of the list:
List =['a','b','c','d','e','f']
print(List[-1])

# range if indexes:
List =['a','b','c','d','e','f']
print(List[2:-1]) #without last element

List =['a','b','c','d','e','f']
print(List[0:])

List =['a','b','c','d','e','f']
print(List[:4])

List =['a','b','c','d','e','f']
print(List[:])

#negative range indexes:
List =['a','b','c','d','e','f']
print(List[-4:-1])

List =['a','b','c','d','e','f']
print(List[-4:])


List =['a','b','c','d','e','f']
print(List[:-1])


